/* tarjeta para mostrar blog*/


import 'package:apfsvalencia/screens/detail_blog_screen.dart';
import 'package:card_swiper/card_swiper.dart';
import 'package:flutter/material.dart';

class CardBlog extends StatelessWidget {
  const CardBlog({Key? key}) : super(key: key);

  @override
  Widget build(BuildContext context) {
    final size = MediaQuery.of(context).size;
    return Container(
      width: double.infinity,
      height: size.height * 0.50,
      child: Swiper(
        itemCount: 10,
        layout: SwiperLayout.STACK,
        itemWidth: size.width * 0.6,
        itemHeight: size.height * 0.4,
        itemBuilder: (_, int index) {
          return GestureDetector(
             onTap: () => Navigator.pushNamed(context, DetailBlogScreen.routerName),
            child: ClipRRect(
              borderRadius: BorderRadius.circular(20),
              child: FadeInImage(
                placeholder: AssetImage('assets/no-image.jpg'),
                image: NetworkImage('https://via.placeholder.com/300x400'),
                fit: BoxFit.cover,
              ),
            ),
          );
        },
      ),
    );
  }
}
